        <h4>The DAPL federal permit is not final.</h4>
          <p>Three federal agencies issued a joint statement on September 9, explaining that:
            <blockquote>“The Army will not authorize constructing the Dakota Access Pipeline on Corps land bordering or under Lake Oahe until it can determine whether it will need to reconsider any of its previous decisions regarding the Lake Oahe site.” <cite>Army Joint Statement, http://bit.ly/2dveZRa</cite></blockquote></p>
      </div>
